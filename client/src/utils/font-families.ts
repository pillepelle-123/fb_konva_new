export function getFontFamily(fontName: string, isBold: boolean, isItalic: boolean): string {
  for (const group of FONT_GROUPS) {
    const font = group.fonts.find(f => f.name === fontName);
    if (font) {
      if (isBold && font.bold) return font.bold;
      if (isItalic && font.italic) return font.italic;
      return font.family;
    }
  }
  return "Arial, sans-serif";
}

export function hasBoldVariant(fontName: string): boolean {
  for (const group of FONT_GROUPS) {
    const font = group.fonts.find(f => f.name === fontName);
    if (font) return !!font.bold;
  }
  return false;
}

export function hasItalicVariant(fontName: string): boolean {
  for (const group of FONT_GROUPS) {
    const font = group.fonts.find(f => f.name === fontName);
    if (font) return !!font.italic;
  }
  return false;
}